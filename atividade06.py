'''
Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.
'''

raio = float(input("\nQual o raio do círculo que deseja calcular: "))

raiocirculo = pow(raio, 2)
pi = 3.14
float(raiocirculo)
float(pi)
resultado = pi * raiocirculo

print("\nValor da área do círculo é: {}".format(resultado))

